package Day9;

public class ExampleIf {
    public static void main(String[] args) {

        int n1 = 10;
        int n2 = 20;

        if(n1>n2){
            System.out.println("n1 is bigger then n2");
        }

        if(n1 == n2){
            System.out.println("n1 equals to n2");
        }

        if(n1 != n2){
            System.out.println("n1 not equals to n2");
        }

        if(n1<n2){
            System.out.println("n2 is bigger then n1");
        }


    }
}
