package Day8;

public class ArithmeticalOperators2 {
    public static void main(String[] args) {

        int number = 10;

//        number = number + 5;
//        System.out.println("number = " + number);

        number += 10; // +=  ===>>> number + 10
        System.out.println("number = " + number);

//        number = number - 3;
//        System.out.println("number = " + number);

        number -= 3; // number = number -3
        System.out.println("number = " + number);

        number *= 2; // number = number *2
        System.out.println("number = " + number);

        number /= 2; // number = number /2
        System.out.println("number = " + number);

        number %= 3; // number = number %3
        System.out.println("number = " + number);
    }
}
