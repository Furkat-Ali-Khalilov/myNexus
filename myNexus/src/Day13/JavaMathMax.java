package Day13;

public class JavaMathMax {
    public static void main(String[] args) {
        // Math.max() works with any numbers. we have to provide two numbers. result will be the biggest number.
        // Math.min() works with any numbers. we have to provide two numbers. result will be the smaller number
        int num1 = 10;
        int num2 = 15;

        System.out.println(Math.max(num2, num1));
        System.out.println(Math.min(num1, num2));



    }
}
