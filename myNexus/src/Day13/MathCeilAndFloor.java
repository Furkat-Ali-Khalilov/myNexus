package Day13;

public class MathCeilAndFloor {
    public static void main(String[] args) {
        // Math.ceil() // works mostly with double or float numbers. rounds number to the upper value
        // Math.floor() // works mostly with double or float numbers. rounds number to the base value

        double db1 = 25.1;
        System.out.println(Math.ceil(db1)); // result 26.0

        double db2 = 25.9;
        System.out.println(Math.floor(db2)); // result 25.0


    }
}
