package Day1;

public class JavaEscapeCharacters {
    public static void main(String[] args) {

        // \n helps to print in the next line
        System.out.println("Hello\nHi");

        // \t helps to leave space between sentences. like TAB function on Microsoft Word.
        System.out.println("Name\tLastName\tPosition");

        System.out.println("Hello\t\tHi");

        // \\ helps to print "\"
        System.out.println("\\Hello\\");

        System.out.println("Hello Hi");

        System.out.println("H" + "e" + "l" + "l" + "o" + " " + "Hi");

        // "Hello"

        // \" helps to print quotation marks "
        System.out.println("\"Hello\"");




    }
}
