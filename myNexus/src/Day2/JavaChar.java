package Day2;

public class JavaChar {
    public static void main(String[] args) {

        char a = 'a';
        System.out.println("a = " + a);

        char dollarSign = '$';
        System.out.println("dollarSign = " + dollarSign);

        char aa = 97;
        System.out.println("aa = " + aa);

        char dollarSign2 = 36;
        System.out.println("dollarSign2 = " + dollarSign2);


        char A = 67;
        char L = 76;
        char I = 73;

        System.out.println("" + A + L + I);



    }
}
