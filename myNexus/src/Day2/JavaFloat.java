package Day2;

public class JavaFloat {
    public static void main(String[] args) {

        // float is to hold containers with the digits. has limitation of digits after "."

        float a = 0.12345678123123F;
        System.out.println("a = " + a);
        float b = 0.12345678123123f;
        System.out.println("b = " + b);



    }
}
